function Pattern(name, beats, steps, channels) {
	this.name = name;
	this.beats = beats;
	this.steps = steps;
	this.channels = channels;
	this.grid = new Array(this.channels);
	for (var i=0; i<this.grid.length; i++) {
		this.grid[i] = new Array(this.steps);
	}
	//alert("Pattern name:"+name+", beats:"+beats+", steps:"+steps+", channels:"+channels+", grid:"+this.grid);
}

